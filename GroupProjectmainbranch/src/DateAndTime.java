/**
 * This class is a basic representation of date and time. The attributes can be represented simply as integers.
 */
    public class DateAndTime {
    private int day;
    private int time;

    // Getters and Setters
    public int getDay() {
        return day;
    }

    public void setDay(int day) {
        this.day = day;
    }

    public int getTime() {
        return time;
    }

    public void setTime(int time) {
        this.time = time;
    }
}
